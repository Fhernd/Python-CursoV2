# Uso de condicionales para determinar si un número es mayor a otro:

numero_1 = int(input('Escriba el primer número: '))
numero_2 = int(input('Escriba el segundo número: '))

if numero_1 > numero_2:
    print('El número {} es mayor a {}.'.format(numero_1, numero_2))

print('El programa ha finalizado.')
